
public class Morechar12 {

    public static void main(String[] args) {
        String input = "Programming";

        char thirdChar = input.charAt(2);
        char fifthChar = input.charAt(4);
        char seventhChar = input.charAt(6);

        System.out.println("3rd character: '" + thirdChar + "'");
        System.out.println("5th character: '" + fifthChar + "'");
        System.out.println("7th character: '" + seventhChar + "'");
    }
}
